package com.in28Minutes.SpringFramework.learnspringframework.game;

public class Pacman implements GamingConsole {

	public void left() {
		System.out.println("run pacman");

	}

	public void right() {
		System.out.println("stop pacman");

	}

	public void up() {
		System.out.println("jump pacman");

	}

	public void down() {
		System.out.println("sit pacman");

	}
}
